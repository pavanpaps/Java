// Java program to find largest
// among two numbers using
// ternary operator

class Greatestof2 {
    public static void main(String args[]) {

        // Variable Declaration
        int a = 10, b = 5, max;

        // Largest among a and b
        max = (a > b) ? a : b;

        // Print the largest Number
        System.out.println("Greater of 2 numbers is " + max);
    }
}
